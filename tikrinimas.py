skaicius = int(input("Iveskite skaiciu: "))
# jeigu salyga
if skaicius > 0:

    # vykdomas blokas, jeigu atitinka salyga
    print(f"skaicius {skaicius} yra teigiamas")

# jeigu ne, tada kita salyga
elif skaicius < 0:
    print(f" skaicius {skaicius} yra neigiamas")

# jeigu vistiek ne, tada kitas blokas
else:
    print("Ivestas nulis")









